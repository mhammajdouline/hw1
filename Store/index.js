

class Makeup{ 
constructor(concealer,mascara,lipgloss)
    {
        this.concealer = concealer;
        this.mascara = mascara;
        this.lipgloss = lipgloss;
     }
}

const beauty = new Makeup('Nars','Dior','Fenty');
console.log(beauty);

